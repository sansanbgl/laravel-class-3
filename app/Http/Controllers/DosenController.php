     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $dosen          = new Dosen;
        $dosen->nip     = $request->nip;
        $dosen->nama    = $request->nama;
        $dosen->pangkat = $request->pangkat;
        $dosen->save();
        return redirect('dosen');
    }

